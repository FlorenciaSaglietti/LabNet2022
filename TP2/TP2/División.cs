using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace TP2
{
    public class División
    {
        public decimal Dividir(decimal numero)
        {
            return numero / 0;
        }

        public decimal Dividir(decimal numero1, decimal numero2)
        {
            return numero1 / numero2;
        }
    }
